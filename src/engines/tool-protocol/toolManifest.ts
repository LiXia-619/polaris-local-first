export function resolveToolExecutorPluginId(kind: ToolActionKind): PolarisToolExecutorPluginId {
  return POLARIS_TOOL_EXECUTOR_BY_ACTION_KIND[kind];
}

export function isToolActionKindHandledByPlugin(
  kind: ToolActionKind,
  pluginId: PolarisToolExecutorPluginId
) {
  return resolveToolExecutorPluginId(kind) === pluginId;
}
