import type { PersonaBaseId, PersonaExpressionId, PersonaRelationshipId } from '../../types/domain';

export type PersonaPromptBaseId = 'blank' | 'subject' | 'null';

type PromptCopy = {
  preview: string;
  prompt: string;
};

type TagPromptCopy = {
  preview: string;
  blank: string;
  subject: string;
};

export function normalizePersonaPromptBase(baseId: PersonaBaseId): PersonaPromptBaseId {
  if (baseId === 'blank' || baseId === 'subject' || baseId === 'null') return baseId;
  return 'subject';
}

export function getPersonaTagPreview(tagId: string) {
  return TAG_PROMPT_COPY[tagId]?.preview ?? '';
}

export function buildPersonaTagPreviewLine(tagIds: string[], fallback = '') {
  const previews = tagIds.map(getPersonaTagPreview).filter(Boolean);
  if (previews.length === 0) return fallback;
  return previews.join('；');
}

